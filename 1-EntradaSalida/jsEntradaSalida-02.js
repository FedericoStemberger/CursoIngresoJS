/* Al presionar el botón 'Mostrar'
debemos lograr tomar un dato por '=prompt()'
y luego mostrarlo por 'alert()'.*/
function mostrar()
{
	var numero;
	
	numero = prompt("Ingrese un número:");
	
	alert("El usuario ingresó : "+numero);
}

